#include <stdio.h>

/**
 * add - two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Sum of a and b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Difference of a and b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Product of a and b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Quotient of a and b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - two numbers
 * @a: First number
 * @b: Second number
 *
 * Return: Remainder of a and b
 */

int mod(int a, int b)
{
	return (a % b);
}
